<?php

declare(strict_types=1);

namespace Kodano\MobileCoupon\Test\Unit\Plugin\DataProvider;

use Kodano\MobileCoupon\Plugin\DataProvider\RuleDataProviderPlugin;
use Magento\Framework\Data\Collection\AbstractDb;
use Magento\SalesRule\Model\Rule;
use Magento\SalesRule\Model\Rule\DataProvider;
use Magento\SalesRule\Model\Coupon;
use PHPUnit\Framework\MockObject\MockObject;
use PHPUnit\Framework\TestCase;

/**
 * Unit tests for RuleDataProviderPlugin
 *
 * Tests the addition of 'on_mobile' field to rule data.
 */
class RuleDataProviderPluginTest extends TestCase
{
    private RuleDataProviderPlugin $plugin;
    private DataProvider|MockObject $dataProvider;
    private AbstractDb|MockObject $collection;

    protected function setUp(): void
    {
        $this->dataProvider = $this->createMock(DataProvider::class);
        $this->collection = $this->getMockBuilder(AbstractDb::class)
            ->disableOriginalConstructor()
            ->onlyMethods(['getItemById'])
            ->getMockForAbstractClass();

        $this->plugin = new RuleDataProviderPlugin();
    }

    /**
     * Create a mock Rule object
     *
     * @return Rule|MockObject
     */
    private function createRuleMock(): Rule|MockObject
    {
        return $this->getMockBuilder(Rule::class)
            ->disableOriginalConstructor()
            ->onlyMethods(['getId', 'getPrimaryCoupon'])
            ->addMethods(['getCouponType', 'getUseAutoGeneration'])
            ->getMock();
    }

    /**
     * Test that on_mobile field is added for specific coupon
     */
    public function testAddsOnMobileFieldForSpecificCoupon(): void
    {
        $ruleId = 1;
        $result = [
            $ruleId => [
                'rule_id' => $ruleId,
                'name' => 'Test Rule',
            ]
        ];

        $primaryCoupon = $this->createMock(Coupon::class);
        $primaryCoupon->expects($this->once())
            ->method('getId')
            ->willReturn(10);
        $primaryCoupon->expects($this->once())
            ->method('getData')
            ->with('on_mobile')
            ->willReturn(1);

        $rule = $this->createRuleMock();
        $rule->expects($this->once())
            ->method('getId')
            ->willReturn($ruleId);
        $rule->expects($this->once())
            ->method('getCouponType')
            ->willReturn(Rule::COUPON_TYPE_SPECIFIC);
        $rule->expects($this->once())
            ->method('getUseAutoGeneration')
            ->willReturn(false);
        $rule->expects($this->once())
            ->method('getPrimaryCoupon')
            ->willReturn($primaryCoupon);

        $this->collection->expects($this->once())
            ->method('getItemById')
            ->with($ruleId)
            ->willReturn($rule);

        $this->dataProvider->expects($this->once())
            ->method('getCollection')
            ->willReturn($this->collection);

        $result = $this->plugin->afterGetData($this->dataProvider, $result);

        $this->assertArrayHasKey('on_mobile', $result[$ruleId]);
        $this->assertSame(1, $result[$ruleId]['on_mobile']);
    }

    /**
     * Test that on_mobile field is not added for auto-generated coupons
     */
    public function testSkipsAutoGeneratedCoupons(): void
    {
        $ruleId = 1;
        $result = [
            $ruleId => [
                'rule_id' => $ruleId,
                'name' => 'Test Rule',
            ]
        ];

        $rule = $this->createRuleMock();
        $rule->expects($this->once())
            ->method('getId')
            ->willReturn($ruleId);
        $rule->expects($this->once())
            ->method('getCouponType')
            ->willReturn(Rule::COUPON_TYPE_SPECIFIC);
        $rule->expects($this->once())
            ->method('getUseAutoGeneration')
            ->willReturn(true);
        $rule->expects($this->never())
            ->method('getPrimaryCoupon');

        $this->collection->expects($this->once())
            ->method('getItemById')
            ->with($ruleId)
            ->willReturn($rule);

        $this->dataProvider->expects($this->once())
            ->method('getCollection')
            ->willReturn($this->collection);

        $result = $this->plugin->afterGetData($this->dataProvider, $result);

        $this->assertArrayNotHasKey('on_mobile', $result[$ruleId]);
    }

    /**
     * Test that on_mobile field is not added for non-specific coupon types
     */
    public function testSkipsNonSpecificCouponTypes(): void
    {
        $ruleId = 1;
        $result = [
            $ruleId => [
                'rule_id' => $ruleId,
                'name' => 'Test Rule',
            ]
        ];

        $rule = $this->createRuleMock();
        $rule->expects($this->once())
            ->method('getId')
            ->willReturn($ruleId);
        $rule->expects($this->once())
            ->method('getCouponType')
            ->willReturn(Rule::COUPON_TYPE_NO_COUPON);
        $rule->expects($this->never())
            ->method('getUseAutoGeneration');
        $rule->expects($this->never())
            ->method('getPrimaryCoupon');

        $this->collection->expects($this->once())
            ->method('getItemById')
            ->with($ruleId)
            ->willReturn($rule);

        $this->dataProvider->expects($this->once())
            ->method('getCollection')
            ->willReturn($this->collection);

        $result = $this->plugin->afterGetData($this->dataProvider, $result);

        $this->assertArrayNotHasKey('on_mobile', $result[$ruleId]);
    }

    /**
     * Test that null result is returned unchanged
     */
    public function testReturnsNullWhenResultIsNull(): void
    {
        $result = $this->plugin->afterGetData($this->dataProvider, null);

        $this->assertNull($result);
    }

    /**
     * Test that empty array result is returned unchanged
     */
    public function testReturnsEmptyArrayWhenResultIsEmpty(): void
    {
        $result = [];

        $result = $this->plugin->afterGetData($this->dataProvider, $result);

        $this->assertSame([], $result);
    }

