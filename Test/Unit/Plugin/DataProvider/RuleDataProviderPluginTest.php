    /**
     * Test that multiple rules are processed correctly
     */
    public function testProcessesMultipleRules(): void
    {
        $ruleId1 = 1;
        $ruleId2 = 2;
        $result = [
            $ruleId1 => [
                'rule_id' => $ruleId1,
                'name' => 'Test Rule 1',
            ],
            $ruleId2 => [
                'rule_id' => $ruleId2,
                'name' => 'Test Rule 2',
            ]
        ];

        // First rule with mobile coupon
        $primaryCoupon1 = $this->createMock(Coupon::class);
        $primaryCoupon1->expects($this->once())
            ->method('getId')
            ->willReturn(10);
        $primaryCoupon1->expects($this->once())
            ->method('getData')
            ->with('on_mobile')
            ->willReturn(1);

        $rule1 = $this->createRuleMock();
        $rule1->expects($this->once())
            ->method('getId')
            ->willReturn($ruleId1);
        $rule1->expects($this->once())
            ->method('getCouponType')
            ->willReturn(Rule::COUPON_TYPE_SPECIFIC);
        $rule1->expects($this->once())
            ->method('getUseAutoGeneration')
            ->willReturn(false);
        $rule1->expects($this->once())
            ->method('getPrimaryCoupon')
            ->willReturn($primaryCoupon1);

        // Second rule with regular coupon
        $primaryCoupon2 = $this->createMock(Coupon::class);
        $primaryCoupon2->expects($this->once())
            ->method('getId')
            ->willReturn(20);
        $primaryCoupon2->expects($this->once())
            ->method('getData')
            ->with('on_mobile')
            ->willReturn(0);

        $rule2 = $this->createRuleMock();
        $rule2->expects($this->once())
            ->method('getId')
            ->willReturn($ruleId2);
        $rule2->expects($this->once())
            ->method('getCouponType')
            ->willReturn(Rule::COUPON_TYPE_SPECIFIC);
        $rule2->expects($this->once())
            ->method('getUseAutoGeneration')
            ->willReturn(false);
        $rule2->expects($this->once())
            ->method('getPrimaryCoupon')
            ->willReturn($primaryCoupon2);

        $this->collection->expects($this->exactly(2))
            ->method('getItemById')
            ->willReturnCallback(function ($id) use ($rule1, $rule2, $ruleId1, $ruleId2) {
                return match ($id) {
                    $ruleId1 => $rule1,
                    $ruleId2 => $rule2,
                    default => null,
                };
            });

        $this->dataProvider->expects($this->exactly(2))
            ->method('getCollection')
            ->willReturn($this->collection);

        $result = $this->plugin->afterGetData($this->dataProvider, $result);

        $this->assertArrayHasKey('on_mobile', $result[$ruleId1]);
        $this->assertSame(1, $result[$ruleId1]['on_mobile']);
        $this->assertArrayHasKey('on_mobile', $result[$ruleId2]);
        $this->assertSame(0, $result[$ruleId2]['on_mobile']);
    }
}

